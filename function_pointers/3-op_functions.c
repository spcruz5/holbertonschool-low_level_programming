#include "3-calc.h"

/**
* op_add - sum
* @a: int
* @b: int
* Return: result.
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
* op_sub - resta
* @a: int
* @b: int
* Return: result
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
* op_mul - multi
* @a: int
* @b: int
* Return: result
**/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
* op_div - divi
* @a: int
* @b: int
* Return: result
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
* op_mod - mod
* @a: int
* @b: int
* Return: result
*/

int op_mod(int a, int b)
{
	return (a % b);
}
